exports.checkboxLogic = (nodejs, vuejs, python, reactjs) => {
  if (nodejs != null) {
    nodejs = true;
  } else {
    nodejs = false;
  }
  if (vuejs != null) {
    vuejs = true;
  } else {
    vuejs = false;
  }
  if (python != null) {
    python = true;
  } else {
    python = false;
  }
  if (reactjs != null) {
    reactjs = true;
  } else {
    reactjs = false;
  }
};
